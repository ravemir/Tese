	@Test
	public void testSegmentation() {
		// Create GPSReadings with speed values
		GPSReading[] speedLimitReads = new GPSReading[]{
				new GPSReading(38.7600279200000, -9.16737057000000, 0.0D, 0.7D),
				new GPSReading(38.7600279200000, -9.16737057000000, 0.0D, 0.7D),
				new GPSReading(38.7600279200000, -9.16737057000000, 0.0D, 0.52D),
				new GPSReading(38.7600279200000, -9.16737057000000, 0.0D, 0.64D),
				new GPSReading(38.7600279200000, -9.16737057000000, 0.0D, 0.83D),
				new GPSReading(38.7600279200000, -9.16737057000000, 0.0D, 1.2D),
				new GPSReading(38.7600279200000, -9.16737057000000, 0.0D, 1.145D),
				new GPSReading(38.7600279200000, -9.16737057000000, 0.0D, 1.269D),
				new GPSReading(38.7600279200000, -9.16737057000000, 0.0D, 1.1113D)};
		StepReading[] speedLimitSteps = new StepReading[]{
				new StepReading(new AccelReading(), 2D, 0.7D),
				new StepReading(new AccelReading(), 2D, 0.7D),
				new StepReading(new AccelReading(), 2D, 0.7D),
				new StepReading(new AccelReading(), 2D, 0.7D),
				new StepReading(new AccelReading(), 2D, 0.7D),
				new StepReading(new AccelReading(), 2D, 0.7D),
				new StepReading(new AccelReading(), 2D, 0.7D),
				new StepReading(new AccelReading(), 2D, 0.7D),
				new StepReading(new AccelReading(), 2D, 0.7D)};
		Double[] expectedSpeedLimits = {
				0.7D, 0.7D, 0.8478D,
				0.8097D, 0.9027D, 1.2362D,
				1.3365D, 1.4505D, 1.4621D};
		
		// Test speed limit values
		for (int i = 0; i < expectedSpeedLimits.length; i++) {
			// Add GPSReadings, along with StepReadings
			gpa.update(null, speedLimitReads[i]);
			gpa.update(null, speedLimitSteps[i]);
			
			// Test speed limit conditions
			assertEquals(expectedSpeedLimits[i], gpa.getCurrentSpeedLimit(), 0.001);
		}
		
		// Test segmentation via speed limit
		assertEquals(speedLimitReads.length, gpa.getCurrentSegment().size());
		gpa.update(null, new GPSReading(38.7600279200000, -9.16737057000000, 0.0D, 3.1416D));
		assertEquals(2.6376D, gpa.getCurrentSpeedLimit(), 0.001);
		assertEquals(1, gpa.getCurrentSegment().size());
		
		// Test segmentation via step conditions
		gpa.update(null, new StepReading(new AccelReading(), 2D, 0.7D));
		gpa.update(null, new GPSReading(38.7600279200000, -9.16737057000000, 0.0D, 1.211D));
		assertEquals(2, gpa.getCurrentSegment().size());
		gpa.update(null, new GPSReading(38.7600279200000, -9.16737057000000, 0.0D, 1.121D));
		assertEquals(1, gpa.getCurrentSegment().size());
	}

